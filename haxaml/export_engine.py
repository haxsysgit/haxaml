def export_frame_to_markdown(project_dir: str, agent: str = "generic") -> str:
    """Export FRAME files to agent-specific markdown.

    Routes through the PromptRecipe pipeline:
      FrameModel.load() → build_recipe() → _render_recipe() → str

    Args:
        project_dir: Path to the project directory.
        agent: Target agent (claude, windsurf, copilot, codex, cursor, gemini, generic).

    Returns:
        Markdown string ready to write to file.
    """
    frame = FrameModel.load(project_dir)
    recipe = build_recipe(frame, agent)
    return _render_recipe(recipe)


def export_to_file(project_dir: str, agent: str = "generic",
                   output_path: Optional[str] = None,
                   override_native: bool = False,
                   overwrite_existing: bool = False) -> str:
    """Export FRAME to agent-specific markdown file.

    Args:
        project_dir: Path to the project directory.
        agent: Target agent.
        output_path: Override output path. If None, uses default for the agent.

    Returns:
        The path of the written file.
    """
    config = AGENT_CONFIGS[agent]
    content = export_frame_to_markdown(project_dir, agent)

    if output_path is None:
        filename = config.get("native_filename") if override_native else config["filename"]
        if not filename:
            filename = config["filename"]
        output_path = str(Path(project_dir) / filename)

    # Ensure parent directory exists
    output = Path(output_path)
    output.parent.mkdir(parents=True, exist_ok=True)

    allow_replace = overwrite_existing or override_native
    if output.exists() and not allow_replace:
        existing = output.read_text(encoding="utf-8", errors="ignore")
        if GENERATED_MARKER not in existing:
            raise FileExistsError(
                f"Refusing to overwrite existing non-Haxaml file: {output}. "
                "Use override options if you explicitly want replacement."
            )

    with open(output_path, "w", encoding="utf-8") as f:
        f.write(content)

    return output_path


