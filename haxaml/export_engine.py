def _render_codex_reasoning_policy(rules: Optional[dict]) -> str:
    profile = _agent_profile(rules)
    reasoning = profile.get("reasoning_policy", {})
    output_contract = profile.get("output_contract", {})
    if not isinstance(reasoning, dict):
        reasoning = {}
    if not isinstance(output_contract, dict):
        output_contract = {}

    private_reasoning = _normalize_text(
        reasoning.get(
            "private_reasoning",
            "Keep private reasoning internal. Do not expose internal deliberation transcripts.",
        )
    )
    public_rationale = _normalize_text(
        reasoning.get(
            "public_rationale",
            "Provide concise rationale tied to file changes, tests, and concrete evidence.",
        )
    )
    checklist = reasoning.get("checklist", [])
    checklist_items = checklist if isinstance(checklist, list) and checklist else [
        "State what changed.",
        "List tests/checks run.",
        "State remaining risks or follow-ups.",
    ]

    required_sections = output_contract.get("required_sections", [])
    required_items = (
        required_sections
        if isinstance(required_sections, list) and required_sections
        else ["Summary", "Changes", "Verification", "Risks"]
    )
    format_notes = output_contract.get("format_notes", [])
    format_items = format_notes if isinstance(format_notes, list) else []

    lines = [
        f"**Private reasoning policy:** {private_reasoning}",
        f"**Public rationale policy:** {public_rationale}",
        "\n**Response checklist:**",
    ]
    lines.extend(f"- {_normalize_text(item)}" for item in checklist_items if _normalize_text(item))
    lines.append("\n**Output contract:**")
    lines.extend(f"- Required section: `{_normalize_text(item)}`" for item in required_items if _normalize_text(item))
    for note in format_items:
        normalized = _normalize_text(note)
        if normalized:
            lines.append(f"- Format note: {normalized}")

    return "\n".join(lines)


def _explicit_examples(profile: dict) -> list[dict]:
    explicit = []
    raw = profile.get("few_shot_examples", [])
    if not isinstance(raw, list):
        return explicit
    for item in raw:
        if not isinstance(item, dict):
            continue
        input_text = _normalize_text(item.get("input"))
        output_text = _normalize_text(item.get("output"))
        if not input_text or not output_text:
            continue
        explicit.append(
            {
                "source": "explicit",
                "title": _normalize_text(item.get("title")) or "Explicit example",
                "input": input_text,
                "output": output_text,
            }
        )
    return explicit


def _derived_examples_from_acts(acts: Optional[dict]) -> list[dict]:
    if not isinstance(acts, dict):
        return []

    derived = []

    decisions = acts.get("decisions", [])
    if isinstance(decisions, list):
        for item in reversed(decisions):
            if not isinstance(item, dict):
                continue
            decision = _normalize_text(item.get("decision"))
            reasoning = _normalize_text(item.get("reasoning"))
            if not decision or not reasoning:
                continue
            derived.append(
                {
                    "source": "derived",
                    "title": f"Decision pattern: {decision}",
                    "input": f"User asks for a decision: {decision}",
                    "output": f"Decision: {decision}\nRationale: {reasoning}",
                }
            )

    completed_tasks = acts.get("completed_tasks", [])
    if isinstance(completed_tasks, list):
        for item in reversed(completed_tasks):
            if not isinstance(item, dict):
                continue
            name = _normalize_text(item.get("name"))
            summary = _normalize_text(item.get("summary"))
            result = _normalize_text(item.get("result")) or "unknown"
            if not name or not summary:
                continue
            derived.append(
                {
                    "source": "derived",
                    "title": f"Completion pattern: {name}",
                    "input": f"User asks for implementation status on '{name}'.",
                    "output": f"Result: {result}\nSummary: {summary}",
                }
            )

    return derived


def _select_examples(rules: Optional[dict], acts: Optional[dict]) -> tuple[list[dict], list[str]]:
    profile = _agent_profile(rules)
    policy = _example_policy(profile)
    max_examples = max(1, policy["max_examples"])
    max_input_chars = max(64, policy["max_input_chars"])
    max_output_chars = max(64, policy["max_output_chars"])
    max_total_chars = max(256, policy["max_total_chars"])
    derived_cap = policy["derived_from_acts_max"]

    explicit = _explicit_examples(profile)
    derived = _derived_examples_from_acts(acts)[:derived_cap]
    pool = explicit + derived

    selected = []
    total_chars = 0
    truncated_fields = 0
    omitted_for_budget = 0

    for item in pool:
        if len(selected) >= max_examples:
            break

        title, title_trunc = _truncate_text(_normalize_text(item.get("title", "")), 120)
        input_text, input_trunc = _truncate_text(_normalize_text(item.get("input", "")), max_input_chars)
        output_text, output_trunc = _truncate_text(_normalize_text(item.get("output", "")), max_output_chars)

        if title_trunc or input_trunc or output_trunc:
            truncated_fields += int(title_trunc) + int(input_trunc) + int(output_trunc)

        candidate_size = len(title) + len(input_text) + len(output_text)
        if selected and total_chars + candidate_size > max_total_chars:
            omitted_for_budget += 1
            break

        selected.append(
            {
                "source": item["source"],
                "title": title,
                "input": input_text,
                "output": output_text,
            }
        )
        total_chars += candidate_size

    notes = [
        "Source precedence: explicit `rules.agent_profile.few_shot_examples` first, then derived snippets from recent `acts` decisions/completed tasks."
    ]
    if len(pool) > len(selected):
        notes.append(
            f"Compaction: showing {len(selected)} of {len(pool)} candidate examples (max_examples={max_examples})."
        )
    if omitted_for_budget:
        notes.append(
            f"Budget guard: stopped after deterministic prefix because max_total_chars={max_total_chars} was reached."
        )
    if truncated_fields:
        notes.append(
            f"Truncation: compacted {truncated_fields} field(s) to per-example limits "
            f"(input={max_input_chars}, output={max_output_chars})."
        )
    return selected, notes


def _render_codex_few_shot_examples(rules: Optional[dict], acts: Optional[dict]) -> str:
    profile = _agent_profile(rules)
    policy = _example_policy(profile)
    examples, notes = _select_examples(rules, acts)

    if not examples:
        if policy.get("fallback_when_empty") == "omit_section":
            return ""
        return (
            "No few-shot examples available.\n\n"
            "Add `rules.agent_profile.few_shot_examples` or keep recent decisions/completed-task "
            "summaries in `.haxaml/acts.yaml` to generate deterministic examples."
        )

    lines = [f"- {note}" for note in notes]
    for idx, example in enumerate(examples, start=1):
        lines.append(f"\n### Example {idx} ({example['source']})")
        lines.append(f"**Input:** {example['input']}")
        lines.append("**Output:**")
        lines.append(example["output"])
    return "\n".join(lines)


def _render_acts(acts: dict, style: str) -> str:
    """Render acts.yaml into markdown."""
    lines = []

    lines.append(f"**Current Phase:** {acts.get('current_phase', 'unknown')}")

    task = acts.get("active_task", {})
    if task:
        lines.append(f"**Active Task:** {task.get('name', 'none')}")
        if task.get("description"):
            lines.append(f"  _{task['description']}_")

    blocked = acts.get("blocked_tasks", [])
    if blocked:
        lines.append("\n**Blocked:**")
        for b in blocked:
            if isinstance(b, dict):
                lines.append(f"- {b.get('name', '?')}: {b.get('reason', '?')}")
            else:
                lines.append(f"- {b}")

    decisions = acts.get("decisions", [])
    if decisions:
        recent = decisions[-5:]
        lines.append(f"\n**Recent Decisions ({len(recent)} of {len(decisions)}):**")
        for d in recent:
            if isinstance(d, dict):
                lines.append(f"- {d.get('decision', '?')} — _{d.get('reasoning', '')}_")
            else:
                lines.append(f"- {d}")

    completed = acts.get("completed_tasks", [])
    if completed:
        lines.append(f"\n**Completed Tasks:** {len(completed)}")

    return "\n".join(lines)


def _render_expect(expect: dict, style: str) -> str:
    """Render expect.yaml into markdown."""
    lines = []

    planning = expect.get("planning", {})
    if planning:
        if planning.get("goal"):
            lines.append(f"**Goal:** {planning['goal']}")
        if planning.get("estimated_runs"):
            lines.append(
                f"**Expected runs:** {planning['estimated_runs']} "
                f"({planning.get('project_size', 'unknown')})"
            )
        if "map_required" in planning:
            state = "required" if planning.get("map_required") else "not required yet"
            lines.append(f"**Map:** {state}")
            if planning.get("map_reason"):
                lines.append(f"  _{planning['map_reason']}_")

    phases = expect.get("phases", [])
    active = [p for p in phases if p.get("status") == "active"]
    if active:
        phase = active[0]
        lines.append(f"**Active Phase:** {phase['name']}")
        if phase.get("done_when"):
            lines.append(f"  _Done when: {phase['done_when']}_")

    upcoming = expect.get("upcoming", [])
    if upcoming:
        lines.append("\n**Upcoming Tasks:**")
        for t in upcoming[:8]:
            pri = t.get("priority", "?")
            lines.append(f"- [{pri}] {t.get('task', '?')}")

    runbook = expect.get("runbook", [])
    visible_runs = [r for r in runbook if r.get("status") in ("active", "planned", "blocked")]
    if visible_runs:
        lines.append("\n**Runbook:**")
        for r in visible_runs[:6]:
            lines.append(f"- Run {r.get('run', '?')} [{r.get('status', '?')}]: {r.get('goal', '?')}")
            if r.get("outcome"):
                lines.append(f"  Outcome: {r['outcome']}")
            if r.get("uses_map"):
                lines.append("  Uses map.yaml: yes")

    map_policy = expect.get("map_policy", {})
    if map_policy.get("agent_instruction"):
        lines.append(f"\n**Map policy:** {map_policy['agent_instruction']}")

    questions = expect.get("open_questions", [])
    if questions:
        blocking_q = [q for q in questions if q.get("blocking")]
        if blocking_q:
            lines.append("\n**⚠ Blocking Questions:**")
            for q in blocking_q:
                lines.append(f"- {q['question']}")

    milestones = expect.get("milestones", [])
    if milestones:
        lines.append("\n**Milestones:**")
        for m in milestones:
            if isinstance(m, dict):
                status = "✓" if m.get("done") else "○"
                lines.append(f"- {status} {m.get('name', '?')}")
            else:
                lines.append(f"- {m}")

    return "\n".join(lines)


def _render_map(map_data: dict, style: str) -> str:
    """Render map.yaml into markdown."""
    lines = []

    modules = map_data.get("modules", [])
    if modules:
        lines.append("| Module | Purpose | Files |")
        lines.append("|--------|---------|-------|")
        for m in modules:
            files = ", ".join(m.get("files", [])[:3])
            if len(m.get("files", [])) > 3:
                files += "..."
            lines.append(f"| {m['name']} | {m.get('purpose', '')} | {files} |")

    deps = map_data.get("dependencies", [])
    if deps:
        lines.append("\n**Dependencies:**")
        for d in deps:
            lines.append(f"- {d['from']} → {d['to']}" + (f" ({d['reason']})" if d.get("reason") else ""))

    impact = map_data.get("impact", [])
    if impact:
        lines.append("\n**Impact Rules:**")
        for i in impact:
            checks = ", ".join(i.get("check", []))
            lines.append(f"- When `{i['when']}` changes → check: {checks}")

    return "\n".join(lines)


