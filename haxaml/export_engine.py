def _load_yaml_safe(path: Path) -> Optional[dict]:
    """Load a YAML file if it exists, return None otherwise."""
    if not path.exists():
        return None
    with open(path) as f:
        return yaml.safe_load(f) or {}


def _resolve_frame_file(project: Path, new_name: str, old_name: str) -> Optional[Path]:
    """Resolve FRAME filename with backward compat."""
    return resolve_frame_file(project, new_name, old_name)


def load_frame(project_dir: str) -> dict:
    """Load all FRAME files from a project directory.

    Returns a dict with keys: facts, rules, acts, map, expect.
    Missing files are None.
    """
    p = Path(project_dir)

    facts_path = _resolve_frame_file(p, "facts.yaml", "brain.yaml")
    rules_path = _resolve_frame_file(p, "rules.yaml", "mind.yaml")
    acts_path = _resolve_frame_file(p, "acts.yaml", "state.yaml")
    map_path = resolve_frame_file(p, "map.yaml")
    expect_path = resolve_frame_file(p, "expect.yaml")

    return {
        "facts": _load_yaml_safe(facts_path) if facts_path else None,
        "rules": _load_yaml_safe(rules_path) if rules_path else None,
        "acts": _load_yaml_safe(acts_path) if acts_path else None,
        "map": _load_yaml_safe(map_path) if map_path else None,
        "expect": _load_yaml_safe(expect_path) if expect_path else None,
    }


def _section(title: str, content: str) -> str:
    """Format a section with title and content."""
    return f"## {title}\n\n{content}"


def _list_items(items: list, prefix: str = "-") -> str:
    """Format a list of items."""
    if not items:
        return "_None defined._"
    return "\n".join(f"{prefix} {item}" for item in items)


def _render_facts(facts: dict, style: str) -> str:
    """Render facts.yaml into markdown."""
    lines = []

    identity = facts.get("identity", {})
    lines.append(f"**Project:** {identity.get('name', 'unknown')}")
    lines.append(f"**Version:** {identity.get('version', '?')}")
    if identity.get("description"):
        lines.append(f"**Description:** {identity['description']}")

    goal = facts.get("goal", {})
    lines.append(f"\n**Purpose:** {goal.get('purpose', 'not defined')}")
    if goal.get("scope"):
        lines.append(f"**Scope:** {goal['scope']}")
    if goal.get("out_of_scope"):
        lines.append(f"**Out of scope:** {goal['out_of_scope']}")

    stack = facts.get("stack", {})
    if stack:
        stack_parts = [f"{k}: {v}" for k, v in stack.items() if v]
        lines.append(f"\n**Stack:** {', '.join(stack_parts)}")

    arch = facts.get("architecture", {})
    if arch:
        lines.append(f"**Architecture:** {arch.get('pattern', '?')} — {arch.get('reasoning', '')}")

    db = facts.get("database", {})
    if db:
        lines.append(f"**Database:** {db.get('type', '?')} ({db.get('connection', '?')})")

    constraints = facts.get("constraints", [])
    if constraints:
        lines.append("\n**Constraints:**")
        for c in constraints:
            lines.append(f"- {c}")

    criteria = facts.get("success_criteria", [])
    if criteria:
        lines.append("\n**Success Criteria:**")
        for c in criteria:
            lines.append(f"- {c}")

    features = facts.get("features", [])
    if features:
        lines.append("\n**Features:**")
        for f in features:
            if isinstance(f, dict):
                status = f.get("status", "?")
                lines.append(f"- [{status}] {f.get('name', '?')}: {f.get('description', '')}")
            else:
                lines.append(f"- {f}")

    return "\n".join(lines)


def _render_rules(rules: dict, style: str) -> str:
    """Render rules.yaml into markdown."""
    lines = []

    # Before task
    before = rules.get("before_task", {})
    read_first = before.get("read_first", [])
    if read_first:
        if style == "imperative":
            lines.append("**Before any task, you MUST read:**")
        else:
            lines.append("**Read first:**")
        for r in read_first:
            lines.append(f"- {r}")

    checks = before.get("check", [])
    if checks:
        lines.append("\n**Pre-task checklist:**")
        for c in checks:
            lines.append(f"- [ ] {c}")

    # Forbidden
    forbidden = rules.get("forbidden", [])
    if forbidden:
        if style == "imperative":
            lines.append("\n**NEVER do the following:**")
        else:
            lines.append("\n**Forbidden actions:**")
        for f in forbidden:
            lines.append(f"- ❌ {f}")

    # Boundaries
    boundaries = rules.get("boundaries", {})
    boundary_rules = boundaries.get("rules", [])
    if boundary_rules:
        lines.append("\n**Boundary rules:**")
        for r in boundary_rules:
            lines.append(f"- {r}")

    # While coding
    while_coding = rules.get("while_coding", {})
    wc_rules = while_coding.get("rules", [])
    if wc_rules:
        lines.append("\n**While coding:**")
        for r in wc_rules:
            lines.append(f"- {r}")

    # After task
    after = rules.get("after_task", {})
    update = after.get("update", [])
    report = after.get("report", [])
    if update or report:
        lines.append("\n**After completing a task:**")
        for u in update:
            lines.append(f"- Update: {u}")
        for r in report:
            lines.append(f"- Report: {r}")

    # Escalation
    escalation = rules.get("escalation", {})
    ask_user = escalation.get("ask_first", escalation.get("ask_user", []))
    if ask_user:
        lines.append("\n**Ask the user before:**")
        for a in ask_user:
            lines.append(f"- {a}")

    return "\n".join(lines)


def _agent_profile(rules: Optional[dict]) -> dict:
    if not isinstance(rules, dict):
        return {}
    profile = rules.get("agent_profile", {})
    return profile if isinstance(profile, dict) else {}


def _normalize_text(value: Any) -> str:
    if value is None:
        return ""
    text = str(value).replace("\r\n", "\n")
    return "\n".join(" ".join(line.split()) for line in text.split("\n")).strip()


def _truncate_text(text: str, max_chars: int) -> tuple[str, bool]:
    if len(text) <= max_chars:
        return text, False
    if max_chars <= 1:
        return "…", True
    return text[: max_chars - 1].rstrip() + "…", True


def _safe_int(value: Any, default: int) -> int:
    return value if isinstance(value, int) and value >= 0 else default


def _example_policy(profile: dict) -> dict:
    policy = dict(DEFAULT_EXAMPLE_POLICY)
    incoming = profile.get("example_policy", {})
    if isinstance(incoming, dict):
        for key, default in DEFAULT_EXAMPLE_POLICY.items():
            if key not in incoming:
                continue
            if isinstance(default, int):
                policy[key] = _safe_int(incoming[key], default)
            elif isinstance(default, str) and isinstance(incoming[key], str):
                policy[key] = incoming[key]
    return policy


