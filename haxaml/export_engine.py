def export_frame_to_markdown(project_dir: str, agent: str = "generic") -> str:
    """Export FRAME files to agent-specific markdown.

    Args:
        project_dir: Path to the project directory.
        agent: Target agent (claude, windsurf, copilot, codex, cursor, gemini, generic).

    Returns:
        Markdown string ready to write to file.
    """
    if agent not in AGENT_CONFIGS:
        raise ValueError(f"Unknown agent: {agent}. Supported: {', '.join(AGENT_CONFIGS)}")

    config = AGENT_CONFIGS[agent]
    style = config["style"]
    frame = load_frame(project_dir)
    parts = []

    # Header
    parts.append(_agent_header(config, agent))

    # Facts
    if frame["facts"]:
        parts.append(_section("Project Facts", _render_facts(frame["facts"], style)))

    if agent == "codex":
        parts.append(_section("Agent Persona", _render_codex_persona(frame["rules"])))
        parts.append(
            _section(
                "Reasoning & Response Policy",
                _render_codex_reasoning_policy(frame["rules"]),
            )
        )
        few_shot = _render_codex_few_shot_examples(frame["rules"], frame["acts"])
        if few_shot:
            parts.append(_section("Few-Shot Examples", few_shot))

    # Rules
    if frame["rules"]:
        parts.append(_section("Rules & Conventions", _render_rules(frame["rules"], style)))

    # Current State
    if frame["acts"]:
        parts.append(_section("Current State", _render_acts(frame["acts"], style)))

    # Module Map
    if frame["map"]:
        parts.append(_section("Module Map", _render_map(frame["map"], style)))

    # Plan
    if frame["expect"]:
        parts.append(_section("What's Expected Next", _render_expect(frame["expect"], style)))

    # Footer
    parts.append(_agent_footer(agent))

    return "\n\n".join(parts)


def export_to_file(project_dir: str, agent: str = "generic",
                   output_path: Optional[str] = None) -> str:
    """Export FRAME to agent-specific markdown file.

    Args:
        project_dir: Path to the project directory.
        agent: Target agent.
        output_path: Override output path. If None, uses default for the agent.

    Returns:
        The path of the written file.
    """
    config = AGENT_CONFIGS[agent]
    content = export_frame_to_markdown(project_dir, agent)

    if output_path is None:
        output_path = str(Path(project_dir) / config["filename"])

    # Ensure parent directory exists
    Path(output_path).parent.mkdir(parents=True, exist_ok=True)

    with open(output_path, "w") as f:
        f.write(content)

    return output_path


def list_agents() -> list[dict]:
    """List all supported agents with their output filenames."""
    return [
        {"name": name, "filename": cfg["filename"], "description": cfg["description"]}
        for name, cfg in AGENT_CONFIGS.items()
    ]
